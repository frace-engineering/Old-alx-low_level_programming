/**
 * swap_int - swaps the value of two integers
 * @a: first int variale
 * @b: second int variable
 * Return: Always 0
 */


void swap_int(int *a, int *b)
{
	int p;
	int q;
	int *ptp;
	int *ptq;

	ptp = &p;
	ptq = &q;

	if (*ptp = p)
	{
		*ptp = q;
	}
	else if (*ptq = q)
	{
		*ptq = p;
	}
}

